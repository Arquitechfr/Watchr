const fs = require("fs");
const path = require("path");

const distDir = path.join(__dirname, "..", "dist");
const webDir = path.join(__dirname, "..", "web");

// 1. Inject Android redirect script into dist/index.html
const distHtmlPath = path.join(distDir, "index.html");
let distHtml = fs.readFileSync(distHtmlPath, "utf8");

const redirectScript = `<script>(function(){try{if(sessionStorage.getItem("watchr_web_bypass")==="1")return}catch(e){}var ua=navigator.userAgent||"";var isAndroidPhone=/Android/i.test(ua)&&/Mobile/i.test(ua)&&!/Tablet/i.test(ua);var isBot=/bot|crawler|spider|googlebot|bingbot|yandex/i.test(ua);var bypass=/[?&]web=1/.test(window.location.search);if(bypass){try{sessionStorage.setItem("watchr_web_bypass","1")}catch(e){}return}if(isAndroidPhone&&!isBot){window.location.replace("https://play.google.com/store/apps/details?id=com.watchr.app")}})();</script>`;

if (!distHtml.includes("watchr_web_bypass")) {
  distHtml = distHtml.replace("</head>", redirectScript + "</head>");
  fs.writeFileSync(distHtmlPath, distHtml);
  console.log("[post-web-build] Injected Android redirect script into dist/index.html");
} else {
  console.log("[post-web-build] Redirect script already present, skipping injection");
}

// 2. Copy static files from web/ to dist/ (excluding index.html which is generated by Expo)
function copyDir(src, dest) {
  if (!fs.existsSync(src)) return;
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);
    if (entry.isDirectory()) {
      copyDir(srcPath, destPath);
    } else if (entry.name !== "index.html") {
      fs.copyFileSync(srcPath, destPath);
      console.log(`[post-web-build] Copied ${path.relative(webDir, srcPath)}`);
    }
  }
}

copyDir(webDir, distDir);
console.log("[post-web-build] Done");
